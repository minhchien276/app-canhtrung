
import 'package:flutter/material.dart';


class PrimaryFont {
  static String fontFamily = 'Inter';
  static TextStyle regular(double size, FontWeight fontWeight) {
    return TextStyle(
      fontFamily: fontFamily,
      fontSize: size,
      fontWeight: fontWeight,
    );
  }
  static TextStyle medium(double size, FontWeight fontWeight) {
    return TextStyle(
      fontFamily: fontFamily,
      fontSize: size,
      fontWeight: fontWeight,
    );
  }

  static TextStyle semibold(double size, FontWeight fontWeight) {
    return TextStyle(
      fontFamily: fontFamily,
      fontSize: size,
      fontWeight: fontWeight,
    );
  }

  static TextStyle bold(double size, FontWeight fontWeight) {
    return TextStyle(
      fontFamily: fontFamily,
      fontSize: size,
      fontWeight: fontWeight,
    );
  }
}



